package cloud.trotter.dashbuddy.ui.fragments.dashhistory.daily

import androidx.recyclerview.widget.DiffUtil
import cloud.trotter.dashbuddy.ui.fragments.dashhistory.common.HistoryPage

class DailyPageDiffCallback : DiffUtil.ItemCallback<HistoryPage.Daily>() {
    override fun areItemsTheSame(oldItem: HistoryPage.Daily, newItem: HistoryPage.Daily): Boolean {
        return oldItem == newItem
    }

    override fun areContentsTheSame(
        oldItem: HistoryPage.Daily,
        newItem: HistoryPage.Daily
    ): Boolean {
        return oldItem == newItem
    }
}