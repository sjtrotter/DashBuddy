package cloud.trotter.dashbuddy.ui.fragments.dashhistory.annual

import androidx.recyclerview.widget.DiffUtil
import cloud.trotter.dashbuddy.ui.fragments.dashhistory.common.HistoryPage

class AnnualPageDiffCallback : DiffUtil.ItemCallback<HistoryPage.Annual>() {
    override fun areItemsTheSame(oldItem: HistoryPage.Annual, newItem: HistoryPage.Annual): Boolean {
        return oldItem.id == newItem.id
    }

    override fun areContentsTheSame(oldItem: HistoryPage.Annual, newItem: HistoryPage.Annual): Boolean {
        return oldItem == newItem
    }
}